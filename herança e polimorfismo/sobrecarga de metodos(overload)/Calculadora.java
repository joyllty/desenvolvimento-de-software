public class Calculadora {

    public static int soma (int n1, int n2){
        return (n1 + n2);
    }

    public static float soma (float n1, float n2){
        return (n1 + n2);
    }

    public static float soma (float n1, float n2, float n3){
        return (n1 + n2 + n3);
    }

    public static double soma (double n1, double n2, double n3, double n4){
        return (n1 + n2 + n3 + n4);
    }
}
